import { z } from "zod";
import { RoleSchema } from "@financeos/shared";

export const PromptOutcomeSchema = z.enum([
  "answered",
  "abstained",
  "blocked",
  "escalated",
  "approved",
  "pending_review",
]);
export type PromptOutcome = z.infer<typeof PromptOutcomeSchema>;

export const PromptLogSchema = z.object({
  id: z.string().uuid(),
  tenantId: z.string(),
  sessionId: z.string(),
  userId: z.string().uuid(),
  userName: z.string(),
  userRole: RoleSchema,
  prompt: z.string(),
  response: z.string().optional(),
  model: z.string(),
  modelVersion: z.string(),
  dataDomainsTouched: z.array(z.string()),
  policiesApplied: z.array(z.string()),
  evidenceUsed: z.array(z.string()),
  outcome: PromptOutcomeSchema,
  abstentionReason: z.string().optional(),
  blockedReason: z.string().optional(),
  escalatedTo: z.string().optional(),
  latencyMs: z.number().int().optional(),
  tokenCount: z.object({
    input: z.number().int(),
    output: z.number().int(),
  }).optional(),
  riskScore: z.number().min(0).max(1).optional(),
  flaggedForReview: z.boolean().default(false),
  reviewNote: z.string().optional(),
  reviewedBy: z.string().optional(),
  reviewedAt: z.string().datetime().optional(),
  timestamp: z.string().datetime(),
});
export type PromptLog = z.infer<typeof PromptLogSchema>;

