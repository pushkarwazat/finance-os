// ─────────────────────────────────────────────────────────────────────────────

export const RetrievalEvalCaseSchema = z.object({
  /** Unique case identifier. */
  id: z.string(),
  /** Question to retrieve evidence for. */
  question: z.string(),
  /** The domain/category of the question. */
  category: z.enum([
    "financial_metrics",
    "policy_compliance",
    "contract_terms",
    "audit_findings",
    "operational_procedures",
    "close_memo",
    "board_reporting",
    "abstention",
  ]),
  /** Expected document IDs that should appear in results. */
  expectedDocumentIds: z.array(z.string()),
  /** Expected chunk content keywords that should appear in top results. */
  expectedKeywords: z.array(z.string()),
  /** Expected retrieval mode for optimal results. */
  optimalMode: z.enum(["dense", "keyword", "hybrid", "metadata_only"]),
  /** Whether the system should abstain (insufficient evidence). */
  shouldAbstain: z.boolean(),
  /** Minimum acceptable confidence (0–1). */
  minConfidence: z.number().min(0).max(1),
  /** Whether at least one citation should be a table. */
  expectsTableCitation: z.boolean(),
  /** Tags for filtering eval suites. */
  tags: z.array(z.string()),
});
export type RetrievalEvalCase = z.infer<typeof RetrievalEvalCaseSchema>;

